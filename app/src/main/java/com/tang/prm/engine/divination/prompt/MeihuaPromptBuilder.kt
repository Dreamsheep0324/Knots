    fun buildUserPrompt(data: MeihuaData, gender: String, birthDate: String, question: String): String {
        val positionNames = listOf("初", "二", "三", "四", "五", "上")

        val yaoLines = data.yaosDetail.sortedByDescending { it.position }.joinToString("\n") { yao ->
            val posName = positionNames[yao.position - 1]
            val trigramName = if (yao.position <= 3) data.mainHexagram.lower else data.mainHexagram.upper
            val element = if (yao.tiYong == "体") data.tiGua.element else data.yongGua.element
            "- ${posName}爻：${yao.yaoType}爻，${trigramName}卦，五行${element}，${yao.tiYong}${if (yao.isChanging) "，动爻" else ""}"
        }

        val interInfo = if (data.interHexagram != null) {
            "互卦${data.interName}（上${data.interHexagram.upper} 下${data.interHexagram.lower}）"
        } else "互卦${data.interName}"

        val changedInfo = if (data.changedHexagram != null) {
            "变卦${data.changedName}（上${data.changedHexagram.upper} 下${data.changedHexagram.lower}）"
        } else "变卦${data.changedName}"

        val changedTiYongInfo = if (data.changedTiGua != null && data.changedYongGua != null) {
            "变卦体用：变体${data.changedTiGua.name}（${data.changedTiGua.element}），变用${data.changedYongGua.name}（${data.changedYongGua.element}），${data.analysis.changedTiYongRelation}"
        } else ""

        val isPureHexagram = data.mainHexagram.upper == data.mainHexagram.lower
        val pureHexagramNote = if (isPureHexagram) "⚠ 此卦为八纯卦（上卦=下卦=${data.mainHexagram.upper}），必须双看比和与六冲\n" else ""

        return """[占卜信息]
占法：梅花易数
性别：$gender
出生日期：$birthDate
时间干支：${data.ganzhi.year}年 ${data.ganzhi.month}月 ${data.ganzhi.day}日 ${data.ganzhi.hour}时
核心结构：主卦${data.originalName}（上${data.mainHexagram.upper} 下${data.mainHexagram.lower}）；$interInfo；$changedInfo
关键提示：体卦${data.tiGua.name}（${data.tiGua.element}·${data.tiGua.nature}）；用卦${data.yongGua.name}（${data.yongGua.element}·${data.yongGua.nature}）；动爻第${data.movingYao.position}爻（${data.movingYao.yaoName}）
结构明细：
- 四时旺衰：${data.analysis.season}季，体卦${data.analysis.tiSeasonState}，用卦${data.analysis.yongSeasonState}
- 体用关系：${data.analysis.tiYongRelation}
- 过程关系：互上${data.analysis.inter2Relation}体，互下${data.analysis.inter1Relation}体
- 结果关系：${data.analysis.changedRelation}
$changedTiYongInfo
${pureHexagramNote}六爻明细：
$yaoLines

[问题]
$question

[断卦要求]
请严格按照[分析流程]的10个步骤进行思考，特别注意第7步八纯卦检查和第8步自检。然后按[输出规则]组织你的回答。记住：说人话，给结论，别绕弯。参考[输出示例]的风格，严禁[反面示例]的写法。"""
    }

    fun getSystemPrompt(): String = SYSTEM_PROMPT
