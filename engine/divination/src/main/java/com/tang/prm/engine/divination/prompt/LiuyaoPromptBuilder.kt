    fun buildUserPrompt(data: LiuyaoData, gender: String, birthDate: String, question: String): String {
        val positionNames = listOf("初", "二", "三", "四", "五", "上")

        val yaoLines = data.yaosDetail.sortedByDescending { it.position }.joinToString("\n") { detail ->
            val posName = positionNames[detail.position - 1]
            val worldResp = when {
                detail.isWorld -> " 世"
                detail.isResponse -> " 应"
                else -> ""
            }
            val voidMark = if (detail.isVoid) " [空]" else ""
            val changeMark = if (detail.isChanging && detail.changedYao != null) {
                val cy = detail.changedYao
                val cyVoid = if (cy.isVoid) " [空]" else ""
                " ○→变${cy.dizhi}${cy.wuxing} ${cy.liuqin}$cyVoid"
            } else ""
            "- ${posName}爻：${detail.yaoType}爻，六亲${detail.sixRelative}，六神${detail.sixGod}，纳甲${detail.najiaDizhi}${detail.wuxing}$worldResp$voidMark$changeMark"
        }

        val changingYaoStr = data.changingYaos.joinToString("；") {
            "${positionNames[it.position - 1]}爻(${it.type})"
        }

        val worldPos = data.yaosDetail.find { it.isWorld }?.position
        val responsePos = data.yaosDetail.find { it.isResponse }?.position

        val specialLine = data.specialPattern?.let { "特殊格局：$it\n" } ?: ""
        val chaoticLine = if (data.isChaotic) "⚠ 乱动卦：${data.chaoticReason ?: ""}\n" else ""

        return """[占卜信息]
占法：六爻
性别：$gender
出生日期：$birthDate
时间干支：${data.ganzhi.year}年 ${data.ganzhi.month}月 ${data.ganzhi.day}日 ${data.ganzhi.hour}时
空亡：${data.voidBranches.joinToString("、")}
核心结构：主卦${data.originalName}（${data.palace.name}宫${data.palace.wuxing}）；变卦${data.changedName}；互卦${data.interName}
动爻：$changingYaoStr
世爻：第${worldPos}爻；应爻：第${responsePos}爻
${specialLine}${chaoticLine}结构明细：
$yaoLines

[问题]
$question

[断卦要求]
请严格按照[分析流程]的9个步骤进行思考，然后按[输出规则]组织你的回答。记住：说人话，给结论，别绕弯。参考[输出示例]的风格，严禁[反面示例]的写法。"""
    }

    fun getSystemPrompt(): String = SYSTEM_PROMPT
